    private void updateDayComboBox() {
        int selectedMonthIndex = monthComboBox.getSelectedIndex() + 1;
        int selectedYear = Integer.parseInt(yearComboBox.getSelectedItem().toString());
        int daysInMonth = getDaysInMonth(selectedMonthIndex, selectedYear);

        // Xóa các mục cũ và thêm các mục mới vào ComboBox ngày
        dayComboBox.removeAllItems();
        for (int i = 1; i <= daysInMonth; i++) {
            dayComboBox.addItem(String.valueOf(i));
        }
    }
    
    private int getDaysInMonth(int month, int year) {
        switch (month) {
            case 4:
            case 6:
            case 9:
            case 11:
                return 30;
            case 2:
                // Kiểm tra năm nhuận
                return (year % 4 == 0 && (year % 100 != 0 || year % 400 == 0)) ? 29 : 28;
            default:
                return 31;
        }
        
    }
    
