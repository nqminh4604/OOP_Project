    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        String ID = idTF.getText().toString();
        String name = nameTF.getText().toString();
        String gender;
        if (maleButton.isSelected()) {
            gender = "male";
        } else if (femaleButton.isSelected()) {
            gender = "female";
        } else {
            gender = "unknown";
        }
        int day = (dayComboBox.getSelectedIndex() + 1);
        int month = (monthComboBox.getSelectedIndex() + 1);
        int year = Integer.parseInt(yearComboBox.getSelectedItem().toString());
        String hometown = hometownTF.getText().toString();
        String department = departmentTF.getText().toString();
        String schoolyear = schoolyearTF.getText().toString();
        for (Student each : StudentManager.List_Of_Student) {
            if (each.getStudentID().equalsIgnoreCase(ID)) {
                JOptionPane.showMessageDialog(rootPane, "This ID is already exist. Try another", "Duplicated ID", JOptionPane.ERROR_MESSAGE);
                return;
            }
        }
        StudentManager.List_Of_Student.add(new Student(ID, name, gender, day,
                month, year, department, schoolyear, hometown));
        if (studentTableForAdd != null) {
            studentTableForAdd.refreshTable();
        }
        

    }//GEN-LAST:event_addButtonActionPerformed

