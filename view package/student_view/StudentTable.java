    private void backupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backupButtonActionPerformed
        // TODO add your handling code here:
        int option = JOptionPane.showConfirmDialog(rootPane, "Do you want to auto save ? ");
        if (option == 0) {
            StudentDAO.writeToExcelFile("student.xlsx");
            JOptionPane.showMessageDialog(rootPane, "Your data has been back up successfully");
        } else if (option == 1) {
            String fileName = JOptionPane.showInputDialog("Enter the path to file:").toString();
            StudentDAO.writeToExcelFile(fileName);
            JOptionPane.showMessageDialog(rootPane, "Your data has been back up successfully");

        } else {
            return;
        }


    }//GEN-LAST:event_backupButtonActionPerformed

