    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = studentTable.getSelectedRow();
        File fileOfStudent = new File("studentSubjectData\\student" + StudentManager.List_Of_Student.get(row).getStudentID() + ".txt");
        fileOfStudent.delete();
        StudentManager.List_Of_Student.remove(row);
        JOptionPane.showMessageDialog(rootPane, "Delete successfully");

        refreshTable();
    }//GEN-LAST:event_deleteButtonActionPerformed

